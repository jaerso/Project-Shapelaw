        <h1>Dialoggestaltung</h1>
        <hr>
        <p>
        Um das Nutzererlebnis in einer interaktiven Anwendung zu verbessern, 
        formulierte Ben Shneiderman die acht goldenen Regeln der Dialoggestaltung. 
        Diese sind inzwischen in die ISO-Norm 9241 eingeflossen. 
        </p>

        <br>

        <h2> 1.	Konsistenz anstreben </h2>
        <p>Verwandte Funktionen wie „Löschen“, „Weiter“ oder „Zurück“ sollten Systemübergreifend 
        immer vorhanden sein, gleich heißen und gleich funktionieren.</p>

        <br>

        <h2> 2.	Abkürzungen für erfahrene Benutzer bereitstellen </h2>
        <p>Erfahrene Nutzer sollten den Interaktionsprozess mit Shortcuts verkürzen können, um schneller arbeiten zu können.
        Abkürzungen, Funktionstaten, versteckte Befehle und Makro-Fähigkeit können hier ebenso sehr hilfreich sein.</p>

        <br>

        <h2> 3.	Informatives Feedback anbieten </h2>
        <p>Zu jeder Systemeingabe des Benutzers sollte auch eine Systemrückmeldung erfolgen und den derzeitigen Status verständlich erläutern. 
        Bei häufigen Systemeingaben kann die Rückmeldung eher moderat ausfallen, nicht so häufige Eingaben sollten besser erklärt werden.</p>

        <br>

        <h2> 4.	Design von Dialogen zur Verdeutlichung der Abgeschlossenheit </h2>
        <p>Aktionssequenzen sollten in Gruppen organisiert sein die einen Anfang, einen Mittelteil und ein Ende aufweisen.
        Das informative Feedback nach einer abgeschlossenen Aktionssequenz gibt dem Benutzer ein Gefühl des ordnungsgemäßen Abschlusses der Aktion 
        und somit ein Gefühl der Entlastung, welches den Weg frei macht für die nächste folgende Aktion.</p>

        <br>

        <h2> 5.	Einfache Fehlerbehandlung anbieten </h2>
        <p>So gut es geht, sollte das System so gestaltet sein, dass der Benutzer keine Fehler machen kann. 
        Falls doch ein Eingabefehler gemacht wird, sollte es eine einfache Fehlerbehandlung geben, die es dem Benutzer ermöglicht den Fehler zu beheben.</p>

        <br>

        <h2> 6.	Möglichkeit zur Stornierung anbieten </h2>
        <p>Diese Möglichkeit nimmt dem Benutzer die Angst etwas falsch machen zu können, und bestärkt ihn gleichzeitig in der Erforschung neuer Menüpunkte. 
        Diese „Undos“ können für einzelne Aktionen, für Dateneinträge oder ganze Menügruppen gelten.</p>

        <br>

        <h2> 7.	Benutzerkontrolle </h2>
        <p>Erfahrene Anwender brauchen das Gefühl, das Sie die Macht über das System haben, und, dass das System auf ihre Eingaben antwortet und sozusagen gehorcht. 
        Das System sollte daher dem Anwender das Gefühl des „Eingebers“ und nicht das des „Antwortenden“ vermitteln.</p>

        <br>

        <h2> 8.	Kurzzeitgedächtnis entlasten </h2>
        <p>Die Begrenzung der menschlichen Informationsverabeitung innerhalb des Kurzzeitgedächtnisses erfordert eine erheblich reduzierte und einfache Anzeige von Bedienelementen.
        Die gleichzeitige Darstellung mehrerer Seiten oder Fenster sollte vermieden werden. Die Fensterbewegungsfrequenz sollte reduziert werden, und die Zeit, 
        die zum Erlernen von Aktionssequenzen benötigt wird, sollte ausreichend bewilligt werden.</p>

